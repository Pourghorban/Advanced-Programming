class MyBaseClass {
    public void myMethod() {
        System.out.println("Base class myMethod()");
    }

    public void myMethod(int num) {
        System.out.println("Base class myMethod(int num)");
    }

    public void myMethod(String str) {
        System.out.println("Base class myMethod(String str)");
    }
}